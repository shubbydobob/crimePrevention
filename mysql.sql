-- 지역 (시, 구)
create table regions(
id int auto_increment primary key,
main_region varchar(50) not null,
sub_region varchar(50) not null,
unique(main_region, sub_region)
);
-- 범죄 종류
create table crimes(
id int auto_increment primary key,
crime_name varchar(100) not null unique
);
-- 범죄 건수
create table crime_occurences (
id int auto_increment primary key,
region_id int not null,
crime_id int not null,
occurences int not null,
foreign key (region_id) references regions(id) on delete cascade,
foreign key (crime_id) references crimes(id) on delete cascade
);
-- 범죄 종류 및 시간
create table CrimeStats(
	Year int,
    CrimeType Varchar(50),
    TimeRange Varchar(20),
    Count int,
    primary key (Year, CrimeType, TimeRange)
);
-- 범죄 종류 및 요일
create table CrimeDays (
	Year int,
    CrimeType Varchar(50),
    Days Varchar(10),
    Count int,
	primary key (Year, CrimeType, Days)
);

-- LSTM 학습값 저장 테이블
-- 요일별
create table system_status (
id int primary key,
last_update_time datetime
);

create table crime_predictions_days(
id int auto_increment primary key,
crime_type varchar(255),
prediction json,
prediction_date datetime,
system_status_id int,
foreign key (system_status_id) references system_status(id)
	on delete cascade
    on update cascade
);

